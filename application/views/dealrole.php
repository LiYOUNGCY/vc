第一步：登录艺术维C

您必须注册成为我们的用户，才能购买作品。如果，您对注册有什么疑问，可以参阅账户注册。

第二步：挑选艺术品

方法一：通过搜索功能查找。
方法二：通过分类导航查找。

第三步：确认购买

方法一：通过 购物车 来购买
方法二：通过 电话申购 来购买

